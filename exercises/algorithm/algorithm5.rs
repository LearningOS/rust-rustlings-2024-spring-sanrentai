use std::collections::VecDeque;

// Define a graph
struct Graph {
    adj: Vec<Vec<usize>>, // 邻接表 adj，用于存储图中每个顶点的相邻顶点列表
}

impl Graph {
    // Create a new graph with n vertices
    fn new(n: usize) -> Self {
        Graph {
            adj: vec![vec![]; n],
        }
    }

    // Add an edge to the graph
    fn add_edge(&mut self, src: usize, dest: usize) {
        self.adj[src].push(dest); 
        self.adj[dest].push(src); 
    }

    // Perform a breadth-first search on the graph, return the order of visited nodes
    // 在图上执行广度优先搜索，并返回访问节点的顺序。
    fn bfs_with_return(&self, start: usize) -> Vec<usize> {
        
		//TODO
        let mut visited = vec![false; self.adj.len()]; // 用于标记节点是否已经被访问过
        let mut visit_order = vec![]; // 用于保存访问节点的顺序
        let mut queue = VecDeque::new(); // 用于存储待访问的节点
        visited[start] = true; // 起点
        queue.push_back(start);
        while let Some(node) = queue.pop_front() {
            visit_order.push(node);

            
            for &adj_node in &self.adj[node] {
                if !visited[adj_node] {
                    // Mark the adjacent node as visited and enqueue it
                    visited[adj_node] = true;
                    queue.push_back(adj_node);
                }
            }
        }
        visit_order
    }
}


#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_bfs_all_nodes_visited() {
        let mut graph = Graph::new(5);
        graph.add_edge(0, 1);
        graph.add_edge(0, 4);
        graph.add_edge(1, 2);
        graph.add_edge(1, 3);
        graph.add_edge(1, 4);
        graph.add_edge(2, 3);
        graph.add_edge(3, 4);

        let visited_order = graph.bfs_with_return(0);
        assert_eq!(visited_order, vec![0, 1, 4, 2, 3]);
    }

    #[test]
    fn test_bfs_different_start() {
        let mut graph = Graph::new(3);
        graph.add_edge(0, 1);
        graph.add_edge(1, 2);

        let visited_order = graph.bfs_with_return(2);
        assert_eq!(visited_order, vec![2, 1, 0]);
    }

    #[test]
    fn test_bfs_with_cycle() {
        let mut graph = Graph::new(3);
        graph.add_edge(0, 1);
        graph.add_edge(1, 2);
        graph.add_edge(2, 0);

        let visited_order = graph.bfs_with_return(0);
        assert_eq!(visited_order, vec![0, 1, 2]);
    }

    #[test]
    fn test_bfs_single_node() {
        let mut graph = Graph::new(1);

        let visited_order = graph.bfs_with_return(0);
        assert_eq!(visited_order, vec![0]);
    }
}

